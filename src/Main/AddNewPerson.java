    // Class for populating data into array list
    public void populateArrayList()
    {
        try
        {
            // Create a file named 'Profile.dat', and reads the content inside file
              FileInputStream file = new FileInputStream("Profile.dat");
              ObjectInputStream inputFile = new ObjectInputStream(file);
              
              // Declare as false first for not reaching end of file
              boolean endOfFile = false;
              
              // Loop while its not end of file
              while (!endOfFile)
              {
                  try
                  {
                      // Add record object into file, while reading the file at the same time 
                        profiles.add((Profile) inputFile.readObject());
                        name = profiles.get(0).getName();
                  }
                  catch (EOFException e)
                  {
                      // Declare endofFile = true if it reaches end of file
                      endOfFile = true;
                  }
                  catch (Exception f)
                  {
                      // Calls an error message dialog if something goes wrong
                      JOptionPane.showMessageDialog(null, f.getMessage());  
                  }
              }
                    
              // Close the file
              inputFile.close();
              new MenuGUI(name, false).setVisible(true);
        }
        catch (IOException e)
        {
            // Calls an error message dialog if something goes wrong
            JOptionPane.showMessageDialog(null, e.getMessage()); 
            //this.setVisible(true);
        }
    };
    
    // Class for saving / writing data into file
    public void saveProfilesToFile()
    {
        try
        {
            // Write contents into file 'Profile.dat'
            FileOutputStream file = new FileOutputStream("Profile.dat");
            ObjectOutputStream outputFile = new ObjectOutputStream(file);
            
            // Check records size, and write the record object into file
            for (int i = 0; i < profiles.size(); i++)
            {
                outputFile.writeObject(profiles.get(i));
            }
            
            // Close 'Records.dat' once writing is over
            outputFile.close();
            
            // Calls a message dialog that the data is succesfully written into file
            JOptionPane.showMessageDialog(null, "Successfully Saved!");
            this.dispose();
        }
        catch (IOException e)
        {
            // Shows error message if an error occured
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    
