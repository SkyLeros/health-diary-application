    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // Check if the text fields are empty
        if (personName.getText().isEmpty() || personBloodType.getText().isEmpty() || personHealthHistory.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(null, "Please enter all fields!");
        }
        else
        {
            // Declare required variables
            // Get the data that is entered inside the text fields, and assign them into the respective variables
            String Name = personName.getText().trim();
            String bloodType = personBloodType.getText().trim();
            String healthHistory = personHealthHistory.getText().trim();
            
            // Create Profile object using Profile Constructor
            Profile profile = new Profile(Name, bloodType, healthHistory);
            
            // Add record object into records arraylist
            profiles.add(profile);
            
            // Calls saveProfilesToFile() method
            saveProfilesToFile();
            new MenuGUI(Name, false).setVisible(true);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

